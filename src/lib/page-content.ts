const DEFAULTS: Record<string, Record<string, unknown>> = {
  home: DEFAULT_HOME_CONTENT,
  about: DEFAULT_ABOUT_CONTENT,
  'how-it-works': DEFAULT_HOW_IT_WORKS_CONTENT,
  contact: DEFAULT_CONTACT_CONTENT,
}

export async function getPageContent(slug: string): Promise<Record<string, unknown>> {
  const defaults = DEFAULTS[slug] ?? {}

  try {
    const supabase = await createClient()
    const { data } = await supabase
      .from('pages')
      .select('content')
      .eq('slug', slug)
      .single()

    if (data?.content && Object.keys(data.content as Record<string, unknown>).length > 0) {
      // Deep merge: use DB values but fill missing keys with defaults
      return { ...defaults, ...(data.content as Record<string, unknown>) }
    }
    return defaults
  } catch {
    return defaults
  }
}
